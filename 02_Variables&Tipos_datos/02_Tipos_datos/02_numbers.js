//Todos los números, independientemente que sean enteros, decimales 
//o exponencias; son NUMBER.

const num1 = 5;     //entero positivo
const num2 = -2;    //entero negativo
const num3 = 1.12;  //decimal positivo
const num4 = -3.5;  //decimal negativo
const num5 = 4e25;  //exponencial
const num6 = 2;

console.log(num1 / num6);   //División
console.log(num1 % num6);   //Potencia (es para saber cuanto nos 
                            //queda de resto en una ivisión)
let numb = 6;

numb = numb + 2;            //Significan lo mismo.
numb += 2                   //Es su forma abreviada
numb *= 0.5
console.log(numb)           //Todos los operados tiene su forma 
                            //abreviada
numb++;     //Incremental
numb--;     //Decremental
